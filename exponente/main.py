# Importamos el módulo 'math', que contiene funciones matemáticas avanzadas
import math

# Usamos la función exp() del módulo math
# math.exp(x) calcula el valor de e elevado a la potencia x
# Donde 'e' es el número de Euler (~2.71828)
# En este caso, exp(2) = e^2 ≈ 7.38905609893065
print(math.exp(2))  # Muestra en pantalla el resultado de e^2
